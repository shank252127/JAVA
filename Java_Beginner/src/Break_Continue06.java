
public class Break_Continue06 {
	public static void main (String args[]) {
		
		//for loop
		for (int x=1;x<=11;x++) {
			if(x==7) {
				//skips the current execution and move to first line againe
				continue;
			}
			System.out.println("Hello for loop");
			if(x==9) {
				//breaks the loop
				break;
			}
			
		}
		
		//for each - will study with array;
		
	}
}
