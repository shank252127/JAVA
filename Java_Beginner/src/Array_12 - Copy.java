
public class Array_12 {

	public static void main(String[] args) {
		
		// array type name = size;
		int nums[]=new int[4];		
		nums[0]=8;		
		System.out.println(nums[3]);
		
		int nums1[]= {1,2,3,4};		
		nums[0]=8;		
		System.out.println(nums1[3]);
		
		//directly reading the value;
		for(int k : nums1)
		{
			System.out.println(k);
		}
		

	}

}
