//everything in java goes to Class - starts with capital letters
public class FirstCode {
//	anything in java executes with main
//	we also need to mentioned type of data it will return
//	void will be used when we don't return anything
//  also mention public or private
//  syntax - public/private static data type main (parameter)
	public static void main(String args[])
	{
		System.out.print("Hello World \n");
		//to break line we add \n		
		System.out.print("Variables \n");
		int a = 5; //there is always range to avoid overflow;
		a=10;				
		// variable can be start with symbol or alpha but not numbers
		System.out.print(a);
		System.out.print("\n");
		double b = 6.6;
		System.out.print(b);
		System.out.print("\n");
		//Fun fact
		char c ='A';
		c=66;
		System.out.print(c);
		
	}
}
